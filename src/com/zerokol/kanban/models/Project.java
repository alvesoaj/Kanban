package com.zerokol.kanban.models;

public class Project {
	private Integer id;
	private String name;

	public Project() {
		super();
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
